package com.parvin.Selenium;

public class StringExample {
    public static void main(String[] args) {

        String razu = "Alamgir Hossain";
        System.out.println(razu.toUpperCase());
        System.out.println(razu.toLowerCase());
        System.out.println(razu.charAt(8));
        System.out.println(razu.length());

        System.out.println("hello \n World");
        System.out.println("hello \t World");
        System.out.println("Hello\\Nisha");
        System.out.println("hello \" World");
        System.out.println("Hello \' World");
    }
}
